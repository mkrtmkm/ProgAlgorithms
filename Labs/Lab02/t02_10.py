def f(n):
    sum = 0                         # O(1)
    for i in range(1, n + 1):       # O(n)
        sum += i                    # O(n)
    return sum                      # O(1)
                                    # T(n) = O(n)

# Дана функція рахує суму натуральних чисел від 1 до n,
# тому можна замінити її на формулу суми арифметичної прогресії

def g(n):
    return n * (n + 1) // 2         # O(1)